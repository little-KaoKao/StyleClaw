from __future__ import annotations

import json
import logging
from pathlib import Path

from styleclaw.core.image_utils import encode_image_for_llm
from styleclaw.core.models import RoundEvaluation
from styleclaw.core.text_utils import clean_json
from styleclaw.providers.llm.base import LLMProvider

logger = logging.getLogger(__name__)

PROMPT_TEMPLATE_PATH = (
    Path(__file__).parent.parent / "providers" / "llm" / "prompts" / "evaluate.md"
)


async def evaluate_round(
    llm: LLMProvider,
    ref_image_paths: list[Path],
    model_images: dict[str, list[Path]],
    round_num: int,
) -> RoundEvaluation:
    system_prompt = (
        PROMPT_TEMPLATE_PATH.read_text()
        .replace("{round_num}", str(round_num))
    )

    content: list[dict] = []

    content.append({"type": "text", "text": "## Reference Images"})
    for img_path in ref_image_paths:
        b64_data, mt = encode_image_for_llm(img_path)
        content.append({
            "type": "image",
            "source": {"type": "base64", "media_type": mt, "data": b64_data},
        })

    for model_id, images in model_images.items():
        content.append({"type": "text", "text": f"## Generated by: {model_id} (Round {round_num})"})
        for img_path in images:
            b64_data, mt = encode_image_for_llm(img_path)
            content.append({
                "type": "image",
                "source": {"type": "base64", "media_type": mt, "data": b64_data},
            })

    content.append({
        "type": "text",
        "text": "Score each model's output against the reference images across all 5 dimensions.",
    })

    messages = [{"role": "user", "content": content}]
    raw = await llm.invoke(system=system_prompt, messages=messages, max_tokens=4096)

    cleaned = clean_json(raw)
    data = json.loads(cleaned)
    evaluation = RoundEvaluation.model_validate(data)
    logger.info(
        "Round %d evaluation: recommendation=%s", round_num, evaluation.recommendation,
    )
    return evaluation
