from __future__ import annotations

import json
import logging
from pathlib import Path

from styleclaw.core.image_utils import image_to_base64, media_type_for
from styleclaw.core.models import ModelEvaluation
from styleclaw.providers.llm.base import LLMProvider

logger = logging.getLogger(__name__)

PROMPT_TEMPLATE_PATH = Path(__file__).parent.parent / "providers" / "llm" / "prompts" / "select_model.md"


async def evaluate_models(
    llm: LLMProvider,
    ref_image_paths: list[Path],
    model_images: dict[str, list[Path]],
) -> ModelEvaluation:
    system_prompt = PROMPT_TEMPLATE_PATH.read_text()

    content: list[dict] = []

    content.append({"type": "text", "text": "## Reference Images"})
    for img_path in ref_image_paths:
        content.append({
            "type": "image",
            "source": {
                "type": "base64",
                "media_type": media_type_for(img_path),
                "data": image_to_base64(img_path),
            },
        })

    for model_id, images in model_images.items():
        content.append({"type": "text", "text": f"## Generated by: {model_id}"})
        for img_path in images:
            content.append({
                "type": "image",
                "source": {
                    "type": "base64",
                    "media_type": media_type_for(img_path),
                    "data": image_to_base64(img_path),
                },
            })

    content.append({
        "type": "text",
        "text": "Score each model's output against the reference images.",
    })

    messages = [{"role": "user", "content": content}]
    raw = await llm.invoke(system=system_prompt, messages=messages, max_tokens=4096)

    cleaned = raw.strip()
    if cleaned.startswith("```"):
        cleaned = cleaned.split("\n", 1)[1]
    if cleaned.endswith("```"):
        cleaned = cleaned.rsplit("```", 1)[0]
    cleaned = cleaned.strip()

    data = json.loads(cleaned)
    evaluation = ModelEvaluation.model_validate(data)
    logger.info("Model evaluation complete. Recommendation: %s", evaluation.recommendation)
    return evaluation
