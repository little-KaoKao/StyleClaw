from __future__ import annotations

import logging
from pathlib import Path

from styleclaw.core.image_utils import build_image_blocks_async
from styleclaw.core.models import ModelEvaluation
from styleclaw.core.text_utils import parse_llm_response
from styleclaw.providers.llm.base import LLMProvider

logger = logging.getLogger(__name__)

PROMPT_TEMPLATE_PATH = Path(__file__).parent.parent / "providers" / "llm" / "prompts" / "select_model.md"


def _build_system_prompt() -> str:
    return PROMPT_TEMPLATE_PATH.read_text(encoding="utf-8")


async def _build_messages(
    ref_image_paths: list[Path],
    model_images: dict[str, list[Path]],
) -> list[dict]:
    all_paths: list[Path | str] = list(ref_image_paths)
    for imgs in model_images.values():
        all_paths.extend(imgs)
    blocks = await build_image_blocks_async(all_paths)
    ref_blocks = blocks[: len(ref_image_paths)]
    rest = blocks[len(ref_image_paths):]

    content: list[dict] = [{"type": "text", "text": "## Reference Images"}]
    content.extend(ref_blocks)
    offset = 0
    for key, images in model_images.items():
        if "/" in key:
            model_id, variant = key.split("/", 1)
            label = f"{model_id} [{variant}]"
        else:
            label = key
        content.append({"type": "text", "text": f"## Generated by: {label}"})
        content.extend(rest[offset : offset + len(images)])
        offset += len(images)
    content.append({
        "type": "text",
        "text": "Score each model×variant combination against the reference images. "
        "Compare prompt-only vs prompt-sref for each model.",
    })
    return [{"role": "user", "content": content}]


def _parse_evaluation(raw_text: str) -> ModelEvaluation:
    return parse_llm_response(raw_text, ModelEvaluation, "model evaluation")


async def evaluate_models(
    llm: LLMProvider,
    ref_image_paths: list[Path],
    model_images: dict[str, list[Path]],
) -> ModelEvaluation:
    raw = await llm.invoke(
        system=_build_system_prompt(),
        messages=await _build_messages(ref_image_paths, model_images),
        max_tokens=4096,
    )
    evaluation = _parse_evaluation(raw)
    logger.info("Model evaluation complete. Recommendation: %s", evaluation.recommendation)
    return evaluation


async def evaluate_models_with_thinking(
    llm: LLMProvider,
    ref_image_paths: list[Path],
    model_images: dict[str, list[Path]],
    thinking_budget: int = 5000,
) -> tuple[ModelEvaluation, str]:
    response = await llm.invoke_with_thinking(
        system=_build_system_prompt(),
        messages=await _build_messages(ref_image_paths, model_images),
        max_tokens=4096,
        thinking_budget=thinking_budget,
    )
    evaluation = _parse_evaluation(response.text)
    logger.info(
        "Model evaluation (with thinking) complete. Recommendation: %s",
        evaluation.recommendation,
    )
    return evaluation, response.thinking
